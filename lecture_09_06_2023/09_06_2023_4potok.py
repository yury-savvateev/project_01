def inter_search(arr,x):
  low = 0
  high = (len(arr)-1)
  while low <= high and x >= arr[low] and x <= arr[high]:
    index = low + int((x - arr[low]) * (high - low) / (arr[high] - arr[low]))
    if arr[index] == x:
      return index
    if arr[index] < x:
      low = index + 1
    else:
      high = index - 1
  return -1


